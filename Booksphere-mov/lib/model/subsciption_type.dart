/// Enum que representa los diferentes tipos de suscripción disponibles.
///
/// Valores posibles:
/// - [FREE]: Suscripción gratuita, sin costo.
/// - [MONTHLY]: Suscripción mensual.
/// - [SEMI_ANNUAL]: Suscripción semestral (cada seis meses).
/// - [YEARLY]: Suscripción anual.
enum SubcriptionType {
  FREE,
  MONTHLY,
  SEMI_ANNUAL,
  YEARLY, PREMIUM,
}
